public class Book {

  public String author;
  public String categories;
  public String publisher;
  public String title;
  public String url;
  public String lastcheckedut;
  public String lastCheckOutBy;

  public String getAuthor() {
    return author;
  }

  public void setAuthor(String author) {
    this.author = author;
  }

  public String getCategories() {
    return categories;
  }

  public void setCategories(String categories) {
    this.categories = categories;
  }

  public String getPublisher() {
    return publisher;
  }

  public void setPublisher(String publisher) {
    this.publisher = publisher;
  }

  public String getTitle() {
    return title;
  }

  public void setTitle(String title) {
    this.title = title;
  }

  public String getUrl() {
    return url;
  }

  public void setUrl(String url) {
    this.url = url;
  }

  public String getLastcheckedut() {
    return lastcheckedut;
  }

  public void setLastcheckedut(String lastcheckedut) {
    this.lastcheckedut = lastcheckedut;
  }

  public String getLastCheckOutBy() {
    return lastCheckOutBy;
  }

  public void setLastCheckOutBy(String lastCheckOutBy) {
    this.lastCheckOutBy = lastCheckOutBy;
  }




}
